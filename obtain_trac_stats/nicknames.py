def getAlias(author):
    if author == "Philip":
        return "philip"
    if author == "gallaecio":
        return "Gallaecio"
    if author == "Josh":
        return "JoshuaJB"
    if author == "Lionkanzen":
        return "Lion_kanzen"
    if author == "stilz_":
        return "stilz"
    return author 

